"""Memoization: The idea of memoization is to trade time for space. E.g.: Instead of plainly running a recursive algorithm for the fibonacci sequence use a list to store every computed
Fibonacci-number and look it up as needed. Will save a huge amount of time in trade for some space.
Works well if:
 1.Optimal substructure: a globally optimal solution can be found by combining optimal solutions to local subproblem.
   -For x > 1: fib(x)=fib(x-1)+fib(x-2)
 2. Overlapping subproblems: findin an optimal solution involves solving the same problem  multiple times.
    -To compute fib(x) we have to compute fib(i<x) many times"""

def fib(n):
    if n == 0 or n == 1:
        return 1
    else:
        return fib(n - 1) + fib(n - 2)

